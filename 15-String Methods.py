#isalpha()
s1 = "anurag"
print(s1.isalpha())
s2 = "anurag45#"
print(s2.isalpha())

#isdigit()
s3 = '123456'
print(s3.isdigit())
s4 = '1234asd56'
print(s4.isdigit())

